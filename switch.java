class Switch {
    public static void main(String[] args) {
        
        int Day = 3;

        switch(Day) {
            case 1:
                System.out.println("Monday");
                break;

            case 2:
                System.out.println("Tuesday");
                break;

            case 3:
                System.out.println("Wednesday");
                break;

            case 4:
                System.out.println("Thursday");
                break;

            default:
                System.out.println("NONE");
        }
    }
}